    private void jButtonAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaActionPerformed
        String curpG = this.jTFCurp.getText();
        String primerNombre = this.jTFPNombre.getText();
        String segundoNombre = this.jTFSNombre.getText();
        String apPatG = this.jTFapPat.getText();
        String apMatG = this.jTFapMat.getText();
        String calleG = this.jTextFieldCalle.getText();
        String numG = this.jTextFieldNumC.getText();
        String colG = this.jTextFieldColonia.getText();
        String cpG = this.jTextFieldCP.getText();
        String cdG = this.jTextFieldCiudad.getText();
        long telCasa = Long.parseLong(this.jTFTelCasa.getText());
        long celular = Long.parseLong(this.jTFCel.getText());
        double sueldo = Double.parseDouble(this.jTextFieldSueldo.getText());

        con.AltaGerente(curpG, primerNombre, segundoNombre, apPatG, apMatG, 
                calleG, numG, colG, cpG, cdG, telCasa, celular, sueldo);
        
        con.mostrarGerente(this.jTableAltaGerente);
        con.mostrarGerente(this.jTableBajaGerente);
        con.mostrarGerente(this.jTableModificarGerente); 
        
        this.jCBCurp.removeAllItems();
       con.consultaGerente(jCBCurp);
       this.jCBCurpCambios.removeAllItems();
       con.consultaGerente(jCBCurpCambios);
    }//GEN-LAST:event_jButtonAltaActionPerformed

