        this.jTextFieldCiudad.setText(r.ciudad());
        this.jTextFieldColonia.setText(r.colonia());
        this.jTextFieldNumC.setText(r.numero() + "");
        this.jTFHoras.setText(rand.nextInt(8) + 3 +"");
        this.jTFTelCasa.setText(r.telefono() + "");
